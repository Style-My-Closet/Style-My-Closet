package com.stylemycloset.recommendation.entity;

public enum Material {
    COTTON,         // 면: 통기성 좋고 흡습성 우수, 여름/중간 날씨 적합
    LINEN,          // 린넨: 통기성 매우 좋음, 여름용
    WOOL,           // 울: 보온성 높음, 겨울용
    CASHMERE,       // 캐시미어: 고급 울, 보온성 우수
    POLYESTER,      // 폴리에스터: 속건성, 내구성, 기능성 스포츠웨어
    NYLON,          // 나일론: 방풍/방수 기능, 경량
    FLEECE,         // 플리스: 보온성 좋음, 가벼운 겨울용
    DOWN,           // 다운: 보온성 최고, 패딩
    SILK,           // 실크: 통기성 좋고 피부 친화적
    LEATHER,        // 가죽: 방풍, 스타일, 일부 보온
    DENIM,          // 데님: 중간 두께, 내구성
    SYNTHETIC,      // 기타 기능성 합성 소재
    RAYON           // 레이온: 부드럽고 통기성 있음
}